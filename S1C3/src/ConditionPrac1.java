public class ConditionPrac1 {
    public static void main(String[] args) {
        char ch = 'f';
        if (ch > 'a' & ch < 'z') {
            System.out.println("x是a到z之间的字符");
        } else {
            System.out.println("不是a到z之间的字符");
        }
    }
}
